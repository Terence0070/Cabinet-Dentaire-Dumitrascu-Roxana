<section id="traitements" class="grey-background">
    <div class="container pb-4 pt-5">
        <hr class="border-top border-dark">
        <h2 class="display-5 text-uppercase fw-bold text-center fw-bold pistache-text">Les traitements</h2>

        <h4 id="carie" class="display-6 classic-text fw-bold pt-3 text-justify">La carie dentaire</h4>

        <p class="classic-text text-justify">Pour soigner une carie, la dent est endormie (sauf si la carie est très superficielle), et la partie cariée est retirée au moyen de divers instruments rotatifs ou manuels. Après cette première étape, la dent présente une cavité qui doit être désinfectée afin de pouvoir la reconstituer à l’abri des microbes, et éviter ainsi tout risque de récidive de la carie.Si la carie est profonde, on applique un pansement isolant , puis on obture la cavité au moyen de matériaux amalgames ou composites qui permettent de reconstituer la forme initiale de la dent et sont suffisamment solides pour résister aux forces de mastication tout en respectant l’équilibre biologique de la bouche.</p>

        <p class="classic-text text-justify">Si la carie a atteint très superficiellement le nerf, que la carie est traitée à temps et que la situation le permet, notamment chez les jeunes, tout est tenté pour conserver la pulpe dentaire, garante de la vitalité de la dent.Le chirurgien-dentiste applique alors un produit réparateur qu’il laisse agir quelques jours avant de reconstituer la dent définitivement.</p>
        
        <h4 id="plombage" class="display-6 classic-text fw-bold pt-5 text-justify">Le plombage et les composites</h4>

        <p class="classic-text text-justify">Le plombage (ou amalgame) est l’un des soins dentaires les plus fréquents. Il sert essentiellement à soigner les caries provoquées par les attaques de bactéries sur l’émail dentaire. Ce soin consiste à enlever la partie atteinte et à la substituer par un plombage qui est en fait, un alliage de plusieurs métaux, dont l’argent, le zinc, le cuivre et du mercure.</p>
        
        <p class="classic-text text-justify">Aujourd’hui il n’est presque plus utilisé à cause de la présence de mercure qui est déconseillée, et on lui substitue le composite, matériau résineux inerte et esthétique.</p>

        <h4 id="devitalisation" class="display-6 classic-text fw-bold pt-5 text-justify">La dévitalisation dentaire</h4>

        <p class="classic-text text-justify">La dévitalisation dentaire est une opération nécessaire dans certains cas de caries sévères proches de la pulpe dentaire ou en cas de chute sur une dent mais aussi de nécrose pulpaire.</p>
        
        <p class="classic-text text-justify">Elle consiste à supprimer le tissu pulpaire au niveau de la racine de la dent et au niveau de la couronne. En d’autres termes, la dent est dépourvue de toutes les terminaisons nerveuses et vasculaires qui sont responsables de la sensibilité et des douleurs.</p>

        <h4 id="extraction" class="display-6 classic-text fw-bold pt-5 text-justify">L’extraction dentaire</h4>

        <p class="classic-text text-justify">Nous y avons recours lorsqu’il n’est plus possible de conserver la dent. Une extraction est notamment nécessaire en cas d’infection comme les kystes dentaires, de fracture de la racine, de dent sévèrement déchaussée et très mobile, de dent incluse, d’accident d’évolution des dents de sagesse, etc.</p>
        
        <p class="classic-text text-justify">On peut également y avoir recours pour un motif orthodontique pour ménager de la place afin de redresser les dents. L’extraction peut être simple avec sous une anesthésie locale ou complexe avec un accès chirurgical et la pose de points de suture. Afin de bénéficier des soins dentaires adéquats et de conserver une parfaite santé bucco-dentaire, assurez-vous de consulter votre dentiste au moins deux fois par an pour des visites de routine.</p>

        <h4 id="couronne" class="display-6 classic-text fw-bold pt-5 text-justify">Les couronnes</h4>

        <p class="classic-text text-justify">Lorsqu’un incident malheureux entraîne une fracture d’une ou de plusieurs dents et par conséquent leurs dévitalisations, la pose d’une couronne dentaire peut s’avérer nécessaire pour corriger le dommage structurel. Il s’agit d’une prothèse dentaire artificielle en céramique (ou porcelaine), en alumine, avec ou sans alliage métallique qui recouvre partiellement ou totalement la dent pour la protéger. C’est le meilleur traitement sur le long terme pour protéger les dents fragiles, car elles sont scellées et donc fixées dans la bouche.</p>

        <h4 id="couronne_implant" class="display-6 classic-text fw-bold pt-5 text-justify">La couronne sur implant </h4>

        <p class="classic-text text-justify">Une couronne dentaire sur implant est un type de prothèse dentaire fixe qui permet de remplacer une dent manquante à l’aide d’un implant dentaire. Lorsqu’une dent est absente ou trop abîmée pour servir de pilier à la couronne, il est en effet possible de poser un implant dentaire qui fera office de racine et de support à l’implant.</p>

        <p class="classic-text text-justify">Confortable et durable, cette solution permet de retrouver un résultat esthétique optimal et la pleine fonctionnalité de son arcade dentaire.</p>

        <h4 id="prothese" class="display-6 classic-text fw-bold pt-5 text-justify">Les prothèses dentaires</h4>

        <p class="classic-text text-justify">La pose d’une prothèse dentaire est un soin nécessaire en cas de perte, en général de plusieurs dents, lorsque nous ne pouvons pas indiquer la pose de couronnes et de bridges, elle remplace les dents manquantes. L’inconvénient majeur de cette technique par rapport à la couronne ou au bridge, c’est qu’elle est amovible et donc moins bien tolérée par le patient. Elles sont parfois en résine ou ont une armature en métal plus sophistiquée (stellite). Les prothèses existent sous plusieurs formes à connaitres :</p>
        <p class="classic-text text-justify">
            <ul class="classic-text text-justify">
                <li>les prothèses partielles provisoires en résine
                <li>les prothèses partielles permanentes avec armature en métal (stellites)
                <li>les prothèses complètes en résine
            </ul>
        </p>

