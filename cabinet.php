<section id="conseils" class="grey-background">
    <div class="container pb-4 pt-5">
        <h2 class="display-5 text-uppercase fw-bold text-center fw-bold pistache-text">Les conseils</h2>
        
        <h4 id="conseil1" class="display-6 classic-text fw-bold pt-3 text-justify">Se brosser les dents après chaque repas</h4>
        
        <p class="classic-text text-justify">À l’origine des caries dentaires et des problèmes de gencives, la plaque dentaire se caractérise par un dépôt blanchâtre où se développent de nombreuses bactéries. Il est indispensable d’éliminer la plaque dentaire plusieurs fois par jour, car celle-ci se reconstitue en quelques heures.</p>
        
        <p class="classic-text text-justify">Idéalement, il est ainsi recommandé de se brosser les dents après chaque repas pour limiter les risques de développer une carie ou une gingivite. Lorsque cela n’est pas possible, nous vous conseillons de vous brosser les dents au minimum deux fois par jour, le matin et le soir avant le coucher.</p>

        <h4 id="conseil2" class="display-6 classic-text fw-bold pt-3 text-justify">Se rendre régulièrement chez son dentiste</h4>

        <p class="classic-text text-justify">Il est important de rendre régulièrement visite à votre dentiste pour un rendez-vous de dentisterie préventive. La fréquence minimale est d’une fois par an.</p>
        
        <p class="classic-text text-justify">À cette occasion, votre dentiste procédera aussi à un examen complet de votre bouche et vérifiera l’état de vos dents et de vos gencives. Si vous avez une santé bucco-dentaire plus fragile, votre dentiste pourrait aussi vous conseiller des rendez-vous plus fréquents.</p>

        <h4 id="conseil3" class="display-6 classic-text fw-bold pt-3 text-justify">Utiliser la bonne technique de brossage</h4>
        
        <p class="classic-text text-justify">Pour un brossage des dents optimal, posez la brosse à dents souple inclinée à 45° au niveau de vos dents et de vos gencives. Brossez ensuite vos dents, puis massez vos gencives. Le brossage doit s’effectuer avec un mouvement de rotation pour maximiser son efficacité.</p>
        
        <p class="classic-text text-justify">Nous vous conseillons de commencer par le haut puis le bas de la mâchoire (ou l’inverse si vous préférez). L’important est surtout de n’oublier aucune dent et aucun recoin de votre bouche !</p>
        
        <p class="classic-text text-justify">Quant à la durée du brossage, un minimum de deux minutes est recommandé pour une bouche saine.</p>

        <h4 id="conseil4" class="display-6 classic-text fw-bold pt-3 text-justify">Choisir une brosse à dents à poils souples</h4>

        <p class="classic-text text-justify">Pour préserver vos dents tout en assurant un brossage efficace, il est primordial de bien choisir sa brosse à dents. Il est important d’en sélectionner une dont la taille et la forme conviennent à votre bouche. L’utilisation d’une brosse à dents à poils souples est recommandée pour ne pas blesser les gencives et abîmer l’émail des dents.</p>
    
        <h4 id="conseil5" class="display-6 classic-text fw-bold pt-3 text-justify">Changer de brosse à dents tous les 3 mois</h4>
        
        <p class="classic-text text-justify">Les poils de votre brosse à dents s’usent un peu à chaque passage. C’est pourquoi il est nécessaire de changer régulièrement de brosse à dents pour maintenir l’efficacité. On recommande généralement de la remplacer tous les 3 mois ou lorsque vous constatez que les poils commencent à se replier ou à s’effiler.</p>
        
        <p class="classic-text text-justify">Passé cette durée, les bénéfices associés au brossage des dents diminuent. Cela peut contribuer au développement plus rapide de la plaque dentaire, à l’origine des caries, des gingivites, mais aussi de la mauvaise haleine.</p>
    
