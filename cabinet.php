        <h2 class="display-5 text-uppercase fw-bold text-center pt-4 pb-3 pistache-text">Les équipements</h2>

        <h4 id="aeropolissage" class="display-6 classic-text fw-bold text-justify pt-3">L'aéropolissage par la technique Air Flow (EMS) ou thérapie du Biofilm</h4>
 
        <p class="classic-text text-justify">L’Aéropolissage est une méthode utilisant de l'air et de l'eau sous pression pour projeter un flux contrôlé de poudre vers les dents .Le jet élimine les colorations et les dépôts bactériens qui se forment à la surface des dents et sous la gencive (Avec un embout spécial)</p>
        <p class="classic-text text-justify">L'Aéropolissage avec la nouvelle poudre AIR FLOWPLUS d'EMS garantit un nettoyage complet et doux des dents, et ce, même dans les zones difficiles d'accès.</p>
        
        <p class="classic-text text-justify">Si la plaque dentaire qui contient des bactéries n'est pas régulièrement éliminée par des soins réguliers, une inflammation des gencives peut en résulter. Le patient peut aller constater que ses gencives sont rouges, enflées et saignent facilement c'est une forme précoce de maladie parodontale : la gingivite.</p>
        
        <p class="classic-text text-justify">Si la gingivite n'est pas traitée, elle évoluera vers une atteinte parodontale plus sévère. La plaque et sa population bactérienne descendent de plus en plus profondément le long de la racine de la dent, la réponse inflammatoire de l'organisme provoque une destruction osseuse. Des espaces que l'on appelle « poche parodontale » se forment entre les gencives et les dents. Trop souvent, le patient ne consultera que lorsque l'os aura tellement diminué qu'une ou plusieurs dents commenceront à bouger.</p>
        
        <p class="classic-text text-justify">Le nettoyage du Biofilm est indispensable par son action à la pérennité de votre santé gingivale et osseuse.</p>

        <h4 id="camera" class="display-6 classic-text fw-bold text-justify pt-5 ">La caméra</h4>

        <p class="classic-text text-justify">Nous réalisons nos empreintes à l’aide d’une caméra optique, afin d'en perfectionner la précision et d’améliorer le confort des patients.</p>
    
        <h4 id="radio-panoramique" class="display-6 classic-text fw-bold text-justify pt-5 ">La radio Panoramique</h4> 

        <p class="classic-text text-justify">La radio panoramique des dents est un examen de débroussaillage courant, réalisé au cabinet du chirurgien-dentiste ou dans un cabinet de radiologie.</p>

        <p class="classic-text text-justify">Elle fournit donc une vision globale de l’état des dents, des mâchoires, des articulations et indirectement des gencives. Les restaurations dentaires (plombage, couronne, implant, etc.) réalisées antérieurement sont également visibles.</p>
        
        <p class="classic-text text-justify">L’examen ne dure que quelques secondes : le patient, dont la tête est maintenue immobile, mord dans un élément fixé à l’appareil pour éviter que les dents de l’arcade supérieure et celles de l’arcade inférieure se chevauchent, avec le risque d’obtenir une image inexploitable.</p>
        
        <p class="classic-text text-justify">Certaines anomalies sont plus facilement visualisables que d’autres. La panoramique étant un examen global, il est parfois nécessaire d’en réaliser d'autres, plus précis, pour compléter cette première approche et affiner le diagnostic.</p>
